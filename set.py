 # Set Operations


# 2.1 Create and access set elements
my_set = {1, 2, 3, 4}
print("Set:", my_set)

# 2.2 Union of elements
set_a = {1, 2, 3}
set_b = {3, 4, 5}
print("Union:", set_a | set_b)

# 2.3 Intersection of elements
print("Intersection:", set_a & set_b)

# 2.4 Difference of elements
print("Difference:", set_a - set_b)
